int get_my_rank() {
    char *rank_str = getenv("MIMPI_RANK");
    if (rank_str != NULL) {
        return atoi(rank_str);
    }
    return -1;
}

int get_world_size() {
    char *size_str = getenv("MIMPI_SIZE");
    if (size_str != NULL) {
        return atoi(size_str);
    }
    return -1;
}

